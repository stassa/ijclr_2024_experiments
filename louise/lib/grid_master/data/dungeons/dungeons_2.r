# Dungeon 11
i <- 11
m <- r.oguelike::generate_dungeon(
  iterations = 5,
  n_row = 20,
  n_col = 20,
  n_rooms = 5,
  is_snake = FALSE,
  is_organic = TRUE,
  seed = NULL,
  colour = TRUE
)
                                                                                                    
dungeon.file <- paste("dungeon_", i, ".map",sep="")
                                                                                                    
write.table(m,  file = dungeon.file, quote = FALSE, sep =" " ,row.names = FALSE, col.names = FALSE)

